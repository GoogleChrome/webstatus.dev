registerIconLibrary('phosphor', {
  mutator: svg => {
    svg.setAttribute('fill', 'currentColor');
  },
  resolver: name => `/public/img/phosphor/assets/icons/${name}.svg`,
});
registerIconLibrary('custom', {
  mutator: svg => {
    svg.setAttribute('fill', 'currentColor');
  },
  resolver: name => `/public/img/custom/${name}.svg`,
});
